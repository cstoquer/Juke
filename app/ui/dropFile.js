function openFiles(e) {
	var files = e.dataTransfer.files;
	var file  = files[0]; // TODO: all files

	var ext = file.name.split('.').pop();

	switch (ext) {
		// case 'json': readJson(file); break;
		case 'mp3':  readMp3(file); break;
	}
}

